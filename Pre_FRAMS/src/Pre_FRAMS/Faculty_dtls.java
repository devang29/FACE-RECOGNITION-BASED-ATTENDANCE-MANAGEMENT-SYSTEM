    private void btn_updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_updateActionPerformed
        // TODO add your handling code here:
        if(!txt_facid.getText().equals("")&&!txt_fname.getText().equals("")&&!txt_lname.getText().equals("")&&cmb_dept.getSelectedIndex()!=0)
        {
            boolean flg=us.update(txt_facid.getText(),txt_fname.getText(),txt_lname.getText(),txt_facid.getText(),"Faculty");
            if(flg==true)
            {
                int l=0;
                InputStream is=null;
                if(fac_img.getIcon()!=null)
                {
                    try
                    {
                    File f=new File("fac.jpg");
                    ImageIcon im2=(ImageIcon) fac_img.getIcon();
                    Image im1=im2.getImage();
                    BufferedImage im=(BufferedImage) im1;
                    ImageIO.write(im,"jpg", f);
                    FileInputStream fis=new FileInputStream(f);
                    is=(InputStream)fis;
                    l=(int) f.length();
                    f.delete();
                    }
                    catch(FileNotFoundException ex)
                    {
                        System.out.println("File Error:"+ex.getMessage());
                    } catch (IOException ex) {
                        Logger.getLogger(Faculty_dtls.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }
                boolean flag=fac.update(txt_facid.getText(),dept.getDeptID(cmb_dept.getSelectedItem().toString()),is,l);
                if(flg==true)
                {
                    JOptionPane.showMessageDialog(this,"Updated successfully...");
                }
                else
                {
                    JOptionPane.showMessageDialog(this,"Can not update...");
                }
            }
            else
            {
                JOptionPane.showMessageDialog(this,"Can not update...");
            }
            
        }
        else
        {
            JOptionPane.showMessageDialog(this,"Enter all the details...");
        }
    }//GEN-LAST:event_btn_updateActionPerformed

    private void btn_viewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_viewActionPerformed
        // TODO add your handling code here:
        String uid=JOptionPane.showInputDialog(this,"Enter FacultyID :");
        if(uid!=null)
        {
        String dep=dept.getDept(fac.getDeptID(uid));
        InputStream is=fac.getImage(uid);
        String fname=us.getFName(uid);
        String lname=us.getLName(uid);
        if(dep==null)
        {
            JOptionPane.showMessageDialog(this,"FacultyID does not exist...");
        }
        else
        {
            if(is!=null)
            {
            try {
                img=ImageIO.read(is);
                fac_img.setText("");
            fac_img.setIcon(new ImageIcon(img));
            } catch (IOException ex) {
                Logger.getLogger(Faculty_dtls.class.getName()).log(Level.SEVERE, null, ex);
            }
            }
            else
            {
                try {
                    BufferedImage i=ImageIO.read(getClass().getResource("/images/not_available.jpg"));
                    fac_img.setText("");
            fac_img.setIcon(new ImageIcon(i));
                } catch (IOException ex) {
                    Logger.getLogger(Faculty_dtls.class.getName()).log(Level.SEVERE, null, ex);
                }
                
            }
            txt_facid.setText(uid);
            txt_fname.setText(fname);
            txt_lname.setText(lname);
            cmb_dept.setSelectedItem(dep);
        }
        }
    }//GEN-LAST:event_btn_viewActionPerformed

