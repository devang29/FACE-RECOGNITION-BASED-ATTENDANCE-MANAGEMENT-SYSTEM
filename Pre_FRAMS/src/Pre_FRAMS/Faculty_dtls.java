    private void btn_saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_saveActionPerformed
        // TODO add your handling code here:
        if(!txt_facid.getText().equals("")&&!txt_fname.getText().equals("")&&!txt_lname.getText().equals("")&&cmb_dept.getSelectedIndex()!=0)
        {
            boolean flg=us.insert(txt_facid.getText(),txt_fname.getText(),txt_lname.getText(),txt_facid.getText(),"Faculty");
            if(flg==true)
            {
                int l=0;
                InputStream is=null;
                if(img!=null)
                {
                    try
                    {
                    File f=new File("fac.jpg");
                    FileInputStream fis=new FileInputStream(f);
                    is=(InputStream)fis;
                    l=(int) f.length();
                    f.delete();
                    }
                    catch(FileNotFoundException ex)
                    {
                        System.out.println("File Error:"+ex.getMessage());
                    }
                }
                boolean flag=fac.insert(txt_facid.getText(),dept.getDeptID(cmb_dept.getSelectedItem().toString()),is,l);
                if(flg==true)
                {
                    JOptionPane.showMessageDialog(this,"Inserted successfully...");
                }
                else
                {
                    JOptionPane.showMessageDialog(this,"Can not insert...");
                }
            }
            else
            {
                JOptionPane.showMessageDialog(this,"Can not insert...");
            }
            
        }
        else
        {
            JOptionPane.showMessageDialog(this,"Enter all the details...");
        }
    }//GEN-LAST:event_btn_saveActionPerformed

