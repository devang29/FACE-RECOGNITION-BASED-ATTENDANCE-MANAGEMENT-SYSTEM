    private void txt_cur_pwdFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txt_cur_pwdFocusLost
        // TODO add your handling code here:
        String old=new String(txt_cur_pwd.getPassword());
        String id=txt_userid.getText();
        if(!id.equals(""))
        {
        try{
           user us=new user();
            
            String status=us.getPassword(id);
            if(status.equals(old))
            {
                lbl_cur_label.setText("Correct Password");
                lbl_cur_label.setForeground(Color.GREEN);
                lbl_cur_label.setVisible(true);
            }
            else
            {
                lbl_cur_label.setText("Incorrect Password");
                lbl_cur_label.setForeground(Color.RED);
                lbl_cur_label.setVisible(true);
                btn_confirm.setEnabled(false);
            }
        }
        catch(Exception e)
        {
            e.printStackTrace();
        }
        }
    }//GEN-LAST:event_txt_cur_pwdFocusLost

