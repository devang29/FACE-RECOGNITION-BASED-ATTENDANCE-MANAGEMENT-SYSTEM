    private void txt_cnf_pwdKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_cnf_pwdKeyReleased
        // TODO add your handling code here:
        String newp=new String(txt_new_pwd.getPassword());
        String cnf=new String(txt_cnf_pwd.getPassword());
        
        if(cnf!=null)
        {
            System.out.println("newp="+newp+",cnf="+cnf);
        if(newp.equals(cnf))
        {
            lbl_cnf_label.setText("Matched");
            lbl_cnf_label.setForeground(Color.GREEN);
            lbl_cnf_label.setVisible(true);
            btn_confirm.setEnabled(true);
        }
        else
        {
            lbl_cnf_label.setText("Not Matched");
            btn_confirm.setEnabled(false);
            lbl_cnf_label.setForeground(Color.RED);
            lbl_cnf_label.setVisible(true);
        }
        }
    }//GEN-LAST:event_txt_cnf_pwdKeyReleased

