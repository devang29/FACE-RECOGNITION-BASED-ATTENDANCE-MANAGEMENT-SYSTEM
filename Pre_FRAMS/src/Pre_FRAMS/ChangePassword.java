    private void btn_confirmActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_confirmActionPerformed
        //TODO add your handling code here:
        String newp=new String(txt_new_pwd.getPassword());
        String id=txt_userid.getText();
        user us=new user();
        if(id!=null)
        {
        boolean flg=us.update(id,us.getFName(id),us.getLName(id),newp,"Admin");
            if(flg==true)
            {
                JOptionPane.showMessageDialog(this,"Password Changed...");
            }
            else
                {
                JOptionPane.showMessageDialog(this,"Can't Change Password, Try again...");
                
            }
        }
        else
        {
            JOptionPane.showMessageDialog(this,"User ID can not null...");
        }
        txt_cnf_pwd.setText("");
        txt_cur_pwd.setText("");
        txt_new_pwd.setText("");
        btn_confirm.setEnabled(false);
        lbl_cnf_label.setVisible(false);
        lbl_cur_label.setVisible(false);
    }//GEN-LAST:event_btn_confirmActionPerformed

