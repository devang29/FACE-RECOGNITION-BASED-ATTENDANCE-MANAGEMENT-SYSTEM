    private void loadCombobox()
    {
        String allsub[]=sub.getAllSub();
        String alldept[]=dept.getAllDept();
        cmb_dept_dept.removeAllItems();
            cmb_dept_fac.removeAllItems();
            cmb_dept_dept.addItem("Select Department");
            cmb_dept_fac.addItem("Select Department");
        if(alldept.length!=0)
        {
            
            for(int i=0;i<alldept.length;i++)
            {
                cmb_dept_dept.addItem(alldept[i]);
                cmb_dept_fac.addItem(alldept[i]);
            }
        }
        cmb_subname_dept.removeAllItems();
            cmb_subname_fac.removeAllItems();
            cmb_subname_dept.addItem("Select Subject");
            cmb_subname_fac.addItem("Select Subject");
        if(allsub.length!=0)
        {
            
            for(int i=0;i<allsub.length;i++)
            {
                cmb_subname_dept.addItem(allsub[i]);
                cmb_subname_fac.addItem(allsub[i]);
            }
        }
    }
