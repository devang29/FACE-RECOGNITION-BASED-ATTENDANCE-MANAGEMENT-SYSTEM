    private void cmb_dept_facItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cmb_dept_facItemStateChanged
        // TODO add your handling code here:
        if(evt.getStateChange()==java.awt.event.ItemEvent.SELECTED)
        {
        if(cmb_dept_fac.getSelectedIndex()!=0)
        {
            String deptid=dept.getDeptID(cmb_dept_fac.getSelectedItem().toString());
            String facid[]=fac.getFacultyIDbyDept(deptid);
            cmb_facultyid.removeAllItems();
            cmb_facultyid.addItem("Select FacultyID");
            cmb_facultyname.removeAllItems();
            cmb_facultyname.addItem("Select FacultyName");
            if(facid.length!=0)
            {
                user us=new user();
                for(int i=0;i<facid.length;i++)
                {
                    String facname=us.getFName(facid[i])+" "+us.getLName(facid[i]);
                    if(facid[i]!=null)
                    {
                        cmb_facultyid.addItem(facid[i]);
                        if(facname!=null)
                           {cmb_facultyname.addItem(facname);}
                    }
                    
                }
            }
        }
        }
    }//GEN-LAST:event_cmb_dept_facItemStateChanged

